using UnityEngine;
using System.Collections;

public class destroyPins : MonoBehaviour {

	private score_text score_keeper;

	// Use this for initialization
	void Start () {
		GameObject gameControllerObject = GameObject.FindWithTag ("Text");
		if (gameControllerObject != null)
		{
			score_keeper = gameControllerObject.GetComponent <score_text>();
		}
		if (score_keeper == null)
		{
			Debug.Log ("Cannot find 'GameController' script");
		}
	}
	
	// Update is called once per frame
	void Update () {

	}

	void OnTriggerEnter (Collider col)
	{
		Debug.Log ("hit " + col.gameObject.name + " on layer " + col.gameObject.layer);
		if(col.gameObject.layer == 8)
		{
			Destroy(col.gameObject);
			score_keeper.incrementScore(); 
		}
	}
}



































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































